
public class TimeTable 
{
	public static void main(String[] args)
	{
		//	Call to the methods
		//	TenTimes();
		OneThreeSevenTimes();
	}
	
	//	Ten Times table Method
	public static void TenTimes()
	{
		for (int i=0;i<=10;i++)
		{
			System.out.println(i + " x " + 10 + " = " + i*10);
		}
	}
	
	//	Ten Times table Method
	public static void OneThreeSevenTimes()
	{
		for (int i=0;i<=10;i++)
		{
			System.out.println(i + " x " + 137 + " = " + i*137);
		}
	}
}
